#include "../libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			count;
	unsigned char	*loc;
	unsigned char	x;

	loc = (unsigned char *)b;
	x = (unsigned char)c;
	count = 0;
	while (count < len)
	{
		loc[count] = x;
		count++;
	}
	return (b);
}
